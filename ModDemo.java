class ModDemo {
 public static void main (String args []) {
  int iresult= 17/5, irem= 17%5;
  double dresult = 17.0/5.0, direm = 17.0%5.0;
  System.out.println("Результат и остаток от деления числа int со знаком /: " + iresult + ", со знаком %: " + irem);
  System.out.println("Результат и остаток от деления числа double со знаком /: " + dresult + ", со знаком %: " + direm);
 }
}
